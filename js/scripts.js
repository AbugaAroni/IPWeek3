$(document).ready(function() {
  $(".clickabledesign").click(function() {
    $("#design").toggle();
    $("#hidedesign").toggle(); });

  $(".clickabledevelopment").click(function() {
    $("#development").toggle();
    $("#hidedevelopment").toggle(); });

  $(".clickablepmanagement").click(function() {
    $("#pmanagement").toggle();
    $("#hidepmanagement").toggle(); });
 });
